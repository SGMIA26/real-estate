/**
 * Gọi hàm này thay cho generateReportPDF() cũ trong real-estate.html
 * @param {Object} propertyData - dữ liệu tài sản từ form + valuation engine
 * @param {string} email - email user (để đặt tên file)
 */
function generateAndDownloadPDF(propertyData, email) {
  try {
    const doc = buildPDFReport(propertyData);
    const filename = `GlobalValuate_${propertyData.reportId || 'BDS'}_${new Date().toISOString().slice(0,10)}.pdf`;
    doc.save(filename);
    return { success: true, filename };
  } catch (err) {
    console.error('[GlobalValuate PDF] Error:', err);
    return { success: false, error: err.message };
  }
}

