function fmt(num) {
  if (!num && num !== 0) return '—';
  return new Intl.NumberFormat('vi-VN').format(Math.round(num));
}

function fmtBillion(num) {
  if (!num && num !== 0) return '—';
  const b = num / 1e9;
  return b >= 1 ? `${b.toFixed(2)} tỷ` : `${(num / 1e6).toFixed(0)} triệu`;
}

function fmtDate(d) {
  const date = d ? new Date(d) : new Date();
  return `${date.getDate().toString().padStart(2,'0')}/${(date.getMonth()+1).toString().padStart(2,'0')}/${date.getFullYear()}`;
}

function generateReportNumber(reportId) {
  const now = new Date();
  const yy = now.getFullYear().toString().slice(-2);
  const mm = (now.getMonth()+1).toString().padStart(2,'0');
  const seq = reportId ? reportId.slice(-4).toUpperCase() : Math.floor(Math.random()*9999).toString().padStart(4,'0');
  return `GV/${yy}${mm}/${seq}`;
}

function numberToVietnamese(num) {
  // Chuyển số → chữ VN (simplified for billions)
  if (!num) return '—';
  const b = Math.round(num / 1e9);
  const m = Math.round((num % 1e9) / 1e6);
  const unitsB = ['', 'một', 'hai', 'ba', 'bốn', 'năm', 'sáu', 'bảy', 'tám', 'chín', 'mười',
    'mười một', 'mười hai', 'mười ba', 'mười bốn', 'mười lăm', 'mười sáu', 'mười bảy', 'mười tám', 'mười chín',
    'hai mươi', 'ba mươi', 'bốn mươi', 'năm mươi', 'sáu mươi', 'bảy mươi', 'tám mươi', 'chín mươi'];
  let result = '';
  if (b > 0 && b < unitsB.length) result += unitsB[b] + ' tỷ';
  else if (b >= 100) result += `${b} tỷ`;
  if (m > 0) result += (result ? ', ' : '') + (m < unitsB.length ? unitsB[m] : m) + ' triệu';
  return result ? result.charAt(0).toUpperCase() + result.slice(1) + ' đồng' : `${fmt(num)} đồng`;
}

